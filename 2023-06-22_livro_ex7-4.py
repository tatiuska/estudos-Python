# Escreva um programa que leia uma string e imprima quantas vezes cada caractere aparece nessa string.
s = "TTAAC"
print(f"Número de vezes que aparece a letra T na string {s}: {s.count('T')}")
print(f"Número de vezes que aparece a letra A na string {s}: {s.count('A')}")
print(f"Número de vezes que aparece a letra C na string {s}: {s.count('C')}")
